#include<iostream>
using namespace std;

int main()
{
    cout<<"Size of Variables"<<endl;
    cout<<"================="<<endl;
    cout<<"size of Int is: "<<sizeof(int)<<endl;
    cout<<"size of char is: "<<sizeof(char)<<endl;
    cout<<"size of float is: "<<sizeof(float)<<endl;
    cout<<"size of double is: "<<sizeof(long double)<<endl;
    cout<<"size of long is: "<<sizeof(long long)<<endl;
    cout<<"size of long int is: "<<sizeof(long int)<<endl;
    cout<<"size of signed int is: "<<sizeof(signed int)<<endl;
    cout<<"size of signed char is: "<<sizeof(signed char)<<endl;
    cout<<"size of short is: "<<sizeof(short)<<endl;
    cout<<"size of bool is: "<<sizeof(bool)<<endl;
    
    return 0;
}